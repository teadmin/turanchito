export const transactionTypes = [
  { type: 'sale', label: 'Venta', slug: 'venta' },
  { type: 'rent', label: 'Alquiler', slug: 'alquiler' }
]

// Generate SEO-optimized keywords and descriptions
export function generateSEOData(city: string, state: string, propertyType: string, transactionType: string) {
  const cityLower = city.toLowerCase()
  const propertyLabel = propertyTypes.find(p => p.type === propertyType)?.label.toLowerCase() || propertyType
  const transactionLabel = transactionTypes.find(t => t.type === transactionType)?.label.toLowerCase() || transactionType
  
  const baseKeywords = [
    `${transactionLabel} ${propertyLabel} en ${city}`,
    `${propertyLabel} en ${transactionLabel} ${city}`,
    `inmuebles en ${city}`,
    `propiedades en ${city}`,
    `bienes raíces ${city}`,
    `${city} inmobiliaria`,
    `${transactionLabel} ${city}`,
    `${propertyLabel} ${city}`,
    `casas en ${city}`,
    `apartamentos en ${city}`
  ]

  const title = `${transactionLabel === 'venta' ? 'Comprar' : 'Alquilar'} ${propertyLabel} en ${city} - Miranchito`
  
  const description = `Encuentra las mejores opciones de ${propertyLabel} en ${transactionLabel} en ${city}, ${state}. Miranchito es tu plataforma inmobiliaria de confianza en Venezuela. ¡Descubre tu próximo hogar hoy!`

  return {
    title,
    description,
    keywords: baseKeywords.join(', '),
    canonical: `/${transactionTypes.find(t => t.type === transactionType)?.slug}-${propertyTypes.find(p => p.type === propertyType)?.slug}-en-${venezuelanCities.find(c => c.city === city)?.slug}`
  }
}

// Generate all possible SEO combinations for Venezuelan cities
export function generateAllSEOCombinations() {
  const combinations: Array<Record<string, unknown>> = []
  
  venezuelanCities.forEach(city => {
    propertyTypes.forEach(property => {
      transactionTypes.forEach(transaction => {
        const seoData = generateSEOData(city.city, city.state, property.type, transaction.type)
        combinations.push({
          ...seoData,
          city: city.city,
          state: city.state,
          propertyType: property.type,
          transactionType: transaction.type,
          slug: `${transaction.slug}-${property.slug}-en-${city.slug}`
        })
      })
    })
  })
  
  return combinations
}